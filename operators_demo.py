#5)Write a python program to work on all logical , bitwise , assignment operators
a=10
b=5 

#logical operators
print(a>5 and b<10)
print(a>5 or b>10)
print(not(a>5))

#Bitwise operators
print(a&b)
print(a|b)
print(a^b)
print(~a)
print(a<<1)
print(a>>1)

#Assignment operators
a+=2
print(a)
b*=3

print(b)